function switchLanguage(lang) {
	document.documentElement.lang = lang;
	const ekn = document.querySelector("#kdigits");
	const een = document.querySelector("#edigits");

	document.querySelectorAll("[data-i18n]").forEach((el) => {
		const key = el.dataset.i18n;
		if (translations[lang] && translations[lang][key]) {
			el.textContent = translations[lang][key];
		}
	});

	localStorage.setItem("lang", lang);
	if (lang == "kn") {
		ekn.style.display = "flex";
		een.style.display = "none";
	} else {
		ekn.style.display = "none";
		een.style.display = "flex";
	}
}

// Handle radio buttons
document.addEventListener("DOMContentLoaded", () => {
	document.querySelectorAll('input[name="lang"]').forEach((input) => {
		input.addEventListener("change", () => {
			switchLanguage(input.value);
		});
	});

	// Restore saved language
	const savedLang = localStorage.getItem("lang") || "en";
	const radio = document.querySelector(`input[value="${savedLang}"]`);
	if (radio) radio.checked = true;

	switchLanguage(savedLang);
});

// video player
document.addEventListener("DOMContentLoaded", () => {
	const videoContainers = document.querySelectorAll(".video-container");

	videoContainers.forEach((container) => {
		container.addEventListener("click", function () {
			// Find the source tag INSIDE the clicked container specifically
			const source = this.querySelector("source");

			if (source) {
				// Get the unique URL from the source tag
				const videoUrl = source.getAttribute("src");

				// Create the Iframe
				const iframe = document.createElement("iframe");
				iframe.setAttribute("src", videoUrl);
				iframe.setAttribute("frameborder", "0");
				iframe.setAttribute("allow", "autoplay; fullscreen");
				iframe.setAttribute("allowfullscreen", "true");

				// Styling to fill the container
				iframe.classList.add(
					"absolute",
					"inset-0",
					"w-full",
					"h-full",
					"z-20",
				);

				// Clear the container (removes old play button/text) and add the unique iframe
				this.innerHTML = "";
				this.appendChild(iframe);
			}
		});
	});
});

